package GameLogic;

/**
 *
 * @author Palalae
 */
public class Player {

    private String name;
    private String colour;

    public Player(String colour) {
        this.colour = colour;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getColour() {
        return colour;
    }

    public void setColour(String colour) {
        this.colour = colour;
    }
}
